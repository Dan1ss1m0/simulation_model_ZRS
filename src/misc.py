import numpy as np


class Position:

    def __init__(self, x, y, z):

        self.x = x
        self.y = y
        self.z = z


class Velocity:

    def __init__(self, x_velocity, y_velocity, z_velocity):

        self.x_velocity = x_velocity
        self.y_velocity = y_velocity
        self.z_velocity = z_velocity
